  /// Get all category names
  static List<String> get categories => patterns.keys.toList();
  
  /// Check if merchant matches a pattern in the category
  static bool matchesPattern(String merchant, String category) {
    final merchantLower = merchant.toLowerCase();
    final categoryPatterns = patterns[category] ?? [];
    
    return categoryPatterns.any((pattern) => 
      merchantLower.contains(pattern.toLowerCase())
    );
  }
  
  /// Find all matching categories for a merchant
  static List<String> findMatchingCategories(String merchant) {
    final matches = <String>[];
    
    for (final category in categories) {
      if (matchesPattern(merchant, category)) {
        matches.add(category);
      }
    }
    
    return matches;
  }
